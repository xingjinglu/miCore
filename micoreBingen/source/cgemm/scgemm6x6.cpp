    v_shr_b32(v4,v0,4);
    v_shl_b32(sst,v0,4);
    v_shl_b32(asld,v3,4);
    v_shl_b32(bsld,v4,4);
    s_shl_b32(s16,slot,11);
    s_shl_b32(s17,gidy,6);
    v_shl_or_b32(v5,v4,9,asld);
    v_shl_or_b32(csld,v1,3,s16);
    v_or_b32(csst,s16,v5);
    s_shl3_add_u32(col,slot,s17);

    VZERO32(c[0][0].x);
    s_sync(vmcnt(0));
    lsst_b128(sst,0x0000,apld);
    lsst_b128(sst,0x1000,bpld);
    s_shl_b32(elda,lda,3);
    s_shl_b32(eldb,ldb,3);
    s_movk_i32(dbtr,0x2000);
    s_sub_u32(k,k,ic(8));
    s_cmpk_u32(k,0,eq);
    s_cmovk_i32(elda,0);
    s_cmovk_i32(eldb,0);
    S_ADD_U64_U32(abase,abase,elda);
    S_ADD_U64_U32(bbase,bbase,eldb);
    s_sync(lgkmcnt(0));
    s_barrier();
    lsld_b128(a[0][0].x,asld,0x0000);
    lsld_b128(a[0][2].x,asld,0x0100);
    lsld_b128(b[0][0].x,bsld,0x1000);
    lsld_b128(b[0][2].x,bsld,0x1100);

__lable__(0);
    lsld_b128(a[1][0].x,asld,0x0200);
    lsld_b128(a[1][2].x,asld,0x0300);
    lsld_b128(b[1][0].x,bsld,0x1200);
    lsld_b128(b[1][2].x,bsld,0x1300);
    v_xor_b32(sst,dbtr,sst);
    s_sync(lgkmcnt(4));
    V_MAC_F32(c[0][0].x,a[0][0].x,b[0][0].x);
    s_setprio(1);
    V_MAC_F32(c[0][0].y,a[0][0].x,b[0][0].y);
    V_MAC_F32(c[0][1].x,a[0][1].x,b[0][0].x);
    V_MAC_F32(c[0][1].y,a[0][1].x,b[0][0].y);
    V_MAC_F32(c[0][2].x,a[0][2].x,b[0][0].x);
    V_MAC_F32(c[0][2].y,a[0][2].x,b[0][0].y);
    V_MAC_F32(c[0][3].x,a[0][3].x,b[0][0].x);
    V_MAC_F32(c[0][3].y,a[0][3].x,b[0][0].y);
    V_MAC_F32(c[1][0].x,a[0][0].x,b[0][1].x);
    V_MAC_F32(c[1][0].y,a[0][0].x,b[0][1].y);
    V_MAC_F32(c[1][1].x,a[0][1].x,b[0][1].x);
